#!/bin/bash
# ==========================================
# 💎 正矿智控 · 一键极速部署 (V2.2 恢复版)
# ==========================================

echo "🚀 [2026-04-01] 正矿系统 (V2.2) · 启动部署程序..."
echo "------------------------------------------------------------"

# 1. 检查物理环境
if [ ! -f .env.justmine ]; then
    echo "❌ [错误] 未检测到 .env.justmine 秘钥金库。请在当前目录下创建它。"
    exit 1
fi

# 2. 核心构建与拉取
echo "⚡ [2/3] 正在让 AI 服务上线 (并行构建)..."
docker-compose up -d --build --remove-orphans || {
    echo "❌ [错误] Docker 启动失败。正在尝试清理系统缓存..."
    docker system prune -f
    docker-compose up -d --build
}

# 3. 健康自检
echo "🚀 [3/3] 正矿智控系统已满血复活！"
echo "------------------------------------------------------------"
docker ps | grep zk-
echo "📊 状态正常，请在企微中发送 '行情' 验证性能。"
